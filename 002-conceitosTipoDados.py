#TIPOS DE DADOS

#Inteiros (int)
print('Inteiros:')
idade = 25
print('idade = ',idade)
quantidade = 100
print('quantidade = ',quantidade)

#Flutantes (float)
print('\nFlutuante:')
preco = 9.99
print('preco = ',preco)
altura = 1.76
print('altura = ',altura)

#Cadeias de texto (strings)
print('\nCadeias de texto:')
nome = "Juan"
print('nome = ',nome)
mensagem = "Holla, mundo!"
print('mensagem = ',mensagem)

#Boleanos
print('\nBoleanos:')
maior_de_idade = True
print('maior_de_idade = ',maior_de_idade)
tem_desconto = False
print('tem_desconto = ',tem_desconto)